//! T0.2.7 Phase 5 — production-stack scale acceptance harness.
//!
//! Runs the exact same production retrieval stack (and the same
//! [`READ_TIME_SYSTEM_PROMPT`]) that `memory.read` dispatches through,
//! across a configurable corpus scale, and asserts the 9-query
//! gauntlet:
//!
//! - 4 contradiction queries (Q11, Q13, Q25, Q26): 4/4 PASS
//! - 2 hard-negative queries (Q21, Q22): 2/2 PASS (vault correctly
//!   reports no relevant content)
//! - 3 short↔long contradiction pairs (S1, S2, S3): 3/3 PASS
//!
//! Verdict criterion: refined per [[structured-contract-user-sees-via-agent]]
//! (locked 2026-05-20). PASS = both literal positions in
//! `synthesis_markdown` OR both in `contradictions_flagged[*].positions`.
//!
//! # Corpus shape
//!
//! Base = `merge_acceptance_100.json` (100 hand-curated memories,
//! identical to the parent `read_pipeline_acceptance.rs` fixture).
//! Augmented with the 6 short↔long pair memories (3 pairs × 2 members)
//! ported verbatim from the t028g spike's [`SHORT_LONG_PAIRS`]. The
//! remainder is synthetic distractors generated by a deterministic
//! seeded PRNG; templates avoid Q21/Q22 hard-negative content words
//! ("Kubernetes", "migration", "dental", "insurance", "policy") and
//! contradiction-pair anchor tokens ("Q1 2027", "Q2 2027", "$89",
//! "$109", "Comcast") so scale-up doesn't poison either signal.
//!
//! # Scale
//!
//! Set via `READ_PIPELINE_ACCEPTANCE_SCALE` env var (default 100).
//! Total corpus size = `max(scale, 106)` (the 100 base + 6 pair members
//! are non-negotiable). Distractors = `total - 106`.
//!
//! # Gating
//!
//! Cron-gated `#[ignore]` — loads the 4.36 GB Qwen-7B GGUF and runs
//! 9-11 LLM inferences. Wall time scales with Qwen latency (dominant
//! cost), not corpus size — ~28 min at scale=100, ~30 min at 1K,
//! ~32 min at 10K. Also `#[cfg(target_os = "windows")]` (Vulkan SDK +
//! Qwen GGUF are Windows-only in V0.2; macOS/Metal + Linux/Vulkan need
//! a t027c spike per ADR-042 Amendment 1).

