// ── The test ──────────────────────────────────────────────────────────────

#[tokio::test(flavor = "multi_thread")]
#[ignore = "heavy: ~28-32 min Qwen-7B inference (latency dominated by LLM, not corpus size); cron-only"]
async fn read_pipeline_scale_acceptance_gauntlet() -> Result<()> {
    let scale = std::env::var(SCALE_ENV)
        .ok()
        .and_then(|s| s.parse::<usize>().ok())
        .unwrap_or(DEFAULT_SCALE);
    let run_started = chrono::Utc::now();
    println!(
        "T0.2.7 Phase 5 scale acceptance — {SCALE_ENV}={scale} — started {}",
        run_started.format("%Y-%m-%d %H:%M:%S UTC")
    );

    // ── Stores + embedder + LLM (mirror parent acceptance test) ─────────
    let dir = tempfile::tempdir()?;
    let key = SqlCipherKey::new("scale-acceptance-passphrase");
    let metadata = MetadataStore::open(dir.path().join("metadata.db"), key).await?;
    let metadata = Arc::new(metadata);
    let vectors_raw = LanceVectorStore::open_with_at_rest_key(
        &dir.path().join("vectors"),
        EMBEDDING_DIM,
        &TEST_AT_REST_KEY,
    )
    .await?;
    let vectors: Arc<dyn VectorStore> = Arc::new(vectors_raw);

    println!("Opening BgeSmallProvider...");
    let bge = open_bge_provider()?;

    println!("Opening Qwen2.5-7B-Instruct with locked V0.2 TuningConfig...");
    let qwen_path = models_dir()?.join(QWEN_MODEL_FILENAME);
    ensure!(qwen_path.exists(), "Qwen-7B GGUF missing at {qwen_path:?}");
    let tuning = TuningConfig {
        n_threads: Some(12),
        n_threads_batch: Some(12),
        n_gpu_layers: Some(99),
        ..TuningConfig::default()
    };
    let qwen_load = Instant::now();
    let qwen = Qwen25_14BProvider::open_with_tuning(&qwen_path, tuning.clone()).await?;
    println!(
        "Qwen-7B ready in {:.1}s (id={}, tuning={:?})",
        qwen_load.elapsed().as_secs_f64(),
        qwen.model_id(),
        tuning
    );

    // ── Corpus assembly ─────────────────────────────────────────────────
    // Base (100) + short-long pairs (6) + distractors (scale - 106 if > 0).
    let base_path = repo_root()?
        .join("crates")
        .join("vault-consolidator")
        .join("tests")
        .join("fixtures")
        .join("merge_acceptance_100.json");
    let mut base_fixture: Vec<MemoryFixtureEntry> =
        serde_json::from_slice(&std::fs::read(&base_path)?)?;
    base_fixture.sort_by(|a, b| a.id.cmp(&b.id));
    let base_count = base_fixture.len();
    let pair_member_count = SHORT_LONG_PAIRS.len() * 2;
    let min_total = base_count + pair_member_count;
    let distractor_count = scale.saturating_sub(min_total);
    let total_target = min_total + distractor_count;
    println!(
        "Corpus plan: base={base_count} + short-long pair members={pair_member_count} + \
         distractors={distractor_count} → total={total_target}"
    );

    // Load query fixture (for Q11-Q26 query text + boundaries).
    let query_fixture_path = vault_retrieval_root()
        .join("test-fixtures")
        .join("merge_acceptance_100_queries.json");
    let query_set: QuerySet = serde_json::from_slice(&std::fs::read(&query_fixture_path)?)?;
    let mut production_queries: Vec<QueryEntry> = Vec::with_capacity(PRODUCTION_QUERY_IDS.len());
    for wanted in PRODUCTION_QUERY_IDS {
        if SHORT_LONG_QUERY_IDS.contains(wanted) {
            // Short-long queries are synthesised inline (not in the JSON
            // fixture). Find the matching pair to source query_text +
            // boundary.
            let pair = SHORT_LONG_PAIRS
                .iter()
                .find(|p| &p.query_id == wanted)
                .with_context(|| format!("short-long pair {wanted} missing"))?;
            production_queries.push(QueryEntry {
                id: pair.query_id.to_string(),
                shape: "short-long".to_string(),
                length_tier: "mixed".to_string(),
                query_text: pair.query_text.to_string(),
                authorized_boundaries: vec![pair.boundary.to_string()],
                expected_memory_ids: Vec::new(),
                notes: String::new(),
            });
        } else {
            let q = query_set
                .queries
                .iter()
                .find(|q| q.id == *wanted)
                .cloned()
                .with_context(|| format!("target {wanted} missing from query fixture"))?;
            production_queries.push(q);
        }
    }

    // ── Insert memories: base → pairs → distractors (deterministic order)
    //
    // T0.2.7 Phase 5 Step 2 (2026-05-22): use `VectorStore::bulk_upsert`
    // for the LanceDB write path (~730× faster than per-row at SCALE=10K
    // per the t028b spike). Metadata writes stay per-row because SQLite
    // single-row inserts are already cheap. Embeddings still computed
    // sequentially (`bge.embed` is per-string); the cost saving is in
    // the LanceDB fragment write + plan compilation amortisation.
    //
    // Use a Vec-of-(id_str, MemoryId) ordered, NOT a HashMap, so insertion
    // order into vector + keyword channels is deterministic. (See
    // tests/abstain_q21_focused.rs for the HashMap-order pitfall that
    // motivated this discipline.)
    println!("Computing embeddings + metadata writes for {total_target} memories...");
    let mut all_memories: Vec<Memory> = Vec::with_capacity(total_target);
    let mut vector_rows = Vec::with_capacity(total_target);

    for entry in &base_fixture {
        let memory = build_memory(&entry.boundary, &entry.content)?;
        let embedding = bge.embed(&entry.content).await?;
        metadata.create_memory(&memory).await?;
        vector_rows.push((memory.id, embedding, memory.boundary.clone()));
        all_memories.push(memory);
    }

    for pair in SHORT_LONG_PAIRS {
        for content in [pair.short_content, pair.long_content] {
            let memory = build_memory(pair.boundary, content)?;
            let embedding = bge.embed(content).await?;
            metadata.create_memory(&memory).await?;
            vector_rows.push((memory.id, embedding, memory.boundary.clone()));
            all_memories.push(memory);
        }
    }

    if distractor_count > 0 {
        let distractors = generate_distractors(distractor_count, DISTRACTOR_SEED);
        for d in &distractors {
            let memory = build_memory(d.boundary, &d.content)?;
            let embedding = bge.embed(&d.content).await?;
            metadata.create_memory(&memory).await?;
            vector_rows.push((memory.id, embedding, memory.boundary.clone()));
            all_memories.push(memory);
        }
    }
    println!(
        "Embeddings + metadata complete: {} memories. Now bulk-upserting to LanceDB...",
        all_memories.len()
    );
    let bulk_t0 = std::time::Instant::now();
    vectors.bulk_upsert(&vector_rows).await?;
    println!(
        "Vector store bulk_upsert: {} rows in {:?}",
        vector_rows.len(),
        bulk_t0.elapsed()
    );

    // ── Production retriever stack ──────────────────────────────────────
    let semantic: Arc<dyn Retriever> = Arc::new(SemanticRetriever::new(
        metadata.clone(),
        bge,
        vectors.clone(),
    ));
    let keyword_index = Arc::new(KeywordIndex::new()?);
    keyword_index.bulk_insert(&all_memories).await?;
    println!("KeywordIndex bulk-loaded {} memories", all_memories.len());
    let keyword: Arc<dyn Retriever> = Arc::new(KeywordRetriever::new(
        keyword_index.clone(),
        metadata.clone(),
    ));
    let hybrid: Arc<dyn Retriever> = Arc::new(HybridRetriever::new(semantic, keyword.clone()));
    let retriever: Arc<dyn Retriever> = Arc::new(AbstainingRetriever::new(hybrid, keyword));

    let llm: Arc<dyn LlmProvider> = Arc::new(qwen);
    let pipeline = ReadPipeline::new(retriever, llm);
    println!(
        "ReadPipeline ready (production stack: Abstain → Hybrid → [Semantic + Keyword], \
         max_candidates={DEFAULT_MAX_CANDIDATES}, system prompt = production v9 default)"
    );

    // ── 2-query warmup (latencies discarded) ────────────────────────────
    println!("\n--- Warmup (Q26 + Q21) ---");
    for warmup_id in ["Q26", "Q21"] {
        let q = production_queries
            .iter()
            .find(|qq| qq.id == warmup_id)
            .expect("warmup query in production set");
        let mut boundaries = Vec::with_capacity(q.authorized_boundaries.len());
        for b in &q.authorized_boundaries {
            boundaries.push(Boundary::new(b)?);
        }
        let rq = ReadQuery {
            query_text: q.query_text.clone(),
            authorized_boundaries: boundaries,
        };
        let start = Instant::now();
        let _ = pipeline.read(rq).await?;
        println!(
            "  warmup {warmup_id}: {:.1}s (discarded)",
            start.elapsed().as_secs_f64()
        );
    }

    // ── 9-query gauntlet ────────────────────────────────────────────────
    println!("\n--- Production gauntlet (9 queries) ---");
    let mut contradiction_passes = 0_usize;
    let mut hard_negative_passes = 0_usize;
    let mut short_long_passes = 0_usize;
    let mut latencies = Vec::with_capacity(production_queries.len());

    for (qi, query) in production_queries.iter().enumerate() {
        let mut boundaries = Vec::with_capacity(query.authorized_boundaries.len());
        for b in &query.authorized_boundaries {
            boundaries.push(Boundary::new(b)?);
        }
        let rq = ReadQuery {
            query_text: query.query_text.clone(),
            authorized_boundaries: boundaries,
        };
        let start = Instant::now();
        let resp: ReadResponse = pipeline.read(rq).await?;
        let latency = start.elapsed();
        latencies.push(latency.as_secs_f64());

        let verdict = assess_query(&query.id, &resp);
        match verdict {
            QualityVerdict::ContradictionPass(detail) => {
                contradiction_passes += 1;
                println!(
                    "[{}/{}] {} contradiction PASS — {:.1}s — {detail}",
                    qi + 1,
                    production_queries.len(),
                    query.id,
                    latency.as_secs_f64()
                );
            }
            QualityVerdict::ContradictionFail(detail) => {
                println!(
                    "[{}/{}] {} contradiction FAIL — {:.1}s — {detail}",
                    qi + 1,
                    production_queries.len(),
                    query.id,
                    latency.as_secs_f64()
                );
            }
            QualityVerdict::HardNegativePass(detail) => {
                hard_negative_passes += 1;
                println!(
                    "[{}/{}] {} hard-negative PASS — {:.1}s — {detail}",
                    qi + 1,
                    production_queries.len(),
                    query.id,
                    latency.as_secs_f64()
                );
            }
            QualityVerdict::HardNegativeFail(detail) => {
                println!(
                    "[{}/{}] {} hard-negative FAIL — {:.1}s — {detail}",
                    qi + 1,
                    production_queries.len(),
                    query.id,
                    latency.as_secs_f64()
                );
            }
            QualityVerdict::ShortLongPass(detail) => {
                short_long_passes += 1;
                println!(
                    "[{}/{}] {} short-long PASS — {:.1}s — {detail}",
                    qi + 1,
                    production_queries.len(),
                    query.id,
                    latency.as_secs_f64()
                );
            }
            QualityVerdict::ShortLongFail(detail) => {
                println!(
                    "[{}/{}] {} short-long FAIL — {:.1}s — {detail}",
                    qi + 1,
                    production_queries.len(),
                    query.id,
                    latency.as_secs_f64()
                );
            }
            QualityVerdict::Observational => {
                println!(
                    "[{}/{}] {} observational — {:.1}s — contradictions_flagged={}",
                    qi + 1,
                    production_queries.len(),
                    query.id,
                    latency.as_secs_f64(),
                    resp.contradictions_flagged.len()
                );
            }
        }
    }

    let mean = latencies.iter().sum::<f64>() / latencies.len() as f64;
    let max = latencies.iter().cloned().fold(0.0_f64, f64::max);
    let min = latencies.iter().cloned().fold(f64::INFINITY, f64::min);
    println!(
        "\nLatency observability: min={min:.1}s · mean={mean:.1}s · max={max:.1}s (NOT asserted; quality is the canonical gate)"
    );

    println!(
        "\nQuality rollup at scale={scale} (total={total_target}): contradictions {}/4, hard-negatives {}/2, short-long {}/3",
        contradiction_passes, hard_negative_passes, short_long_passes
    );
    assert_eq!(
        contradiction_passes, 4,
        "scale={scale}: 4/4 contradictions required, got {contradiction_passes}/4"
    );
    assert_eq!(
        hard_negative_passes, 2,
        "scale={scale}: 2/2 hard-negatives required, got {hard_negative_passes}/2"
    );
    assert_eq!(
        short_long_passes, 3,
        "scale={scale}: 3/3 short-long required, got {short_long_passes}/3"
    );
    println!("Production-stack scale acceptance PASSED at scale={scale} ✓");
    Ok(())
}

