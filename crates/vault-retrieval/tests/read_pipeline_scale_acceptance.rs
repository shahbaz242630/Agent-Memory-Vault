// ── Distractor generation (deterministic seeded) ─────────────────────────
//
// Templates AVOID two classes of token overlap with the production
// queries:
//
// 1. Exact protected tokens — Q21/Q22 hard-neg content words
//    ("Kubernetes", "migration", "dental", "insurance", "policy") and
//    contradiction-pair anchors ("Q1 2027", "Q2 2027", "$89", "$109",
//    "Comcast", "$2,500", "$4,200", "$145").
//
// 2. SEMANTIC OVERLAP with non-protected query vocab. The Phase 5
//    SCALE=1000 run (2026-05-21) caught Q25 failing because earlier
//    distractor templates included "Q3 roadmap review", "monthly
//    metrics readout", "code review process update", "Meeting notes
//    ... milestone" etc. — words like "roadmap", "milestone",
//    "review", "monthly" semantically collide with Q25's "product
//    roadmap" / "milestone dates" + Q26's "monthly budget review" +
//    S3's "office Wi-Fi monthly cost". At 894 distractor density
//    these flooded the BGE + BM25 candidate windows and pushed the
//    real GA-launch contradiction pair members out of the top-K
//    surfaced to the LLM.
//
//    Current pool deliberately uses office content that is
//    semantically distant from the query domains (facilities,
//    cafeteria, supply, social events).

struct SplitMix64 {
    state: u64,
}

impl SplitMix64 {
    fn new(seed: u64) -> Self {
        Self { state: seed }
    }
    fn next_u64(&mut self) -> u64 {
        self.state = self.state.wrapping_add(0x9E37_79B9_7F4A_7C15);
        let mut z = self.state;
        z = (z ^ (z >> 30)).wrapping_mul(0xBF58_476D_1CE4_E5B9);
        z = (z ^ (z >> 27)).wrapping_mul(0x94D0_49BB_1331_11EB);
        z ^ (z >> 31)
    }
    fn pick<'a, T>(&mut self, slice: &'a [T]) -> &'a T {
        &slice[(self.next_u64() as usize) % slice.len()]
    }
}

